@contextlib.contextmanager
def timer():
  """Time the execution of a context block.

  Yields:
    None
  """
  start = time.time()
  # Send control back to the context block
  yield timer()
  end = time.time()
  print('Elapsed: {:.2f}s'.format(end - start))

